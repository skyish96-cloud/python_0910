#연산자
result = 4+3
print(result)

result = 4-3
print(result)

result = 7/2
print(result)
# 나누기 : 소숫점도 반영해서 입력
#실수 나눗셈, 결과는 항상 float 타입

result = 7//2 # 소숫점 버림
print(result)
# //는 소수점 버리고 파이썬에만 있는 연산임
# 정수끼리 나누면 정수 반환
# //: 정수끼리 연산하면 int, 실수가 포함되면 float

result = 7%2 # 나머지 연산
print(result)
# % : 음수에서도 사용할 수 있음

result = 7**2 # **이 들어가면 제곱연산이다.
print(result)