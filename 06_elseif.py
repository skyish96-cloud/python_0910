# 콜라, 생수, 오렌지주스, 식혜, 이온음료
item = '냉커피'

if item == '콜라':
    print(f'{item}가 나왔습니다.')
elif item == '생수':
    print(f'{item}가 나왔습니다.')
elif item == '오렌지주스':
    print(f'{item}가 나왔습니다.')
elif item == '식혜':
    print(f'{item}가 나왔습니다.')
elif item == '이온음료':
        print(f'{item}가 나왔습니다.')
else:
    print (f'{item}는 없습니다.')
# 이퀄스는 자바에서만 사용한다.
# JAVA에서 만약(if) 또 다른(else) 이라고 넣었으면 Python에서는 elif로 표시한다.